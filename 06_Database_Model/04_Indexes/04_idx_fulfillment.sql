-- Store orders pending acknowledgment (partial index)
CREATE INDEX IF NOT EXISTS idx_store_orders_pending_ack
  ON store_orders(campaign_id, created_at)
  WHERE status = 'PENDING' AND deleted_at IS NULL;

COMMENT ON INDEX idx_store_orders_pending_ack IS
  'Find orders awaiting PSP acknowledgment';

-- Store orders acknowledged but not shipped (partial index)
CREATE INDEX IF NOT EXISTS idx_store_orders_acknowledged
  ON store_orders(campaign_id, acknowledged_at DESC)
  WHERE status = 'ACKNOWLEDGED' AND deleted_at IS NULL;

COMMENT ON INDEX idx_store_orders_acknowledged IS
  'Find acknowledged orders awaiting shipment';

-- Store orders by generation metadata
CREATE INDEX IF NOT EXISTS idx_store_orders_generated_by
  ON store_orders(generated_by_user_id, generated_at DESC)
  WHERE generated_by_user_id IS NOT NULL AND deleted_at IS NULL;

COMMENT ON INDEX idx_store_orders_generated_by IS
  'Find orders generated by a specific user or process';

-- Store orders cancelled (partial index for cancelled order tracking)
CREATE INDEX IF NOT EXISTS idx_store_orders_cancelled
  ON store_orders(cancelled_at DESC)
  WHERE cancelled_at IS NOT NULL AND deleted_at IS NULL;

COMMENT ON INDEX idx_store_orders_cancelled IS
  'Find cancelled orders, sorted by cancellation time';

