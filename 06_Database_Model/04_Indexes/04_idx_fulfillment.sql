-- Store orders by campaign and store (composite for finding store's orders in a campaign)
CREATE INDEX IF NOT EXISTS idx_store_orders_campaign_store
  ON store_orders(campaign_id, store_id)
  WHERE deleted_at IS NULL;

COMMENT ON INDEX idx_store_orders_campaign_store IS
  'Find all orders for a specific store in a specific campaign';

-- Store orders by campaign and status (for campaign order dashboards)
CREATE INDEX IF NOT EXISTS idx_store_orders_campaign_status
  ON store_orders(campaign_id, status, created_at DESC)
  WHERE deleted_at IS NULL;

COMMENT ON INDEX idx_store_orders_campaign_status IS
  'Find orders for a campaign filtered by status, sorted by creation time';

-- Store orders by PSP order reference (for PSP integration lookups)
CREATE INDEX IF NOT EXISTS idx_store_orders_psp_ref
  ON store_orders(psp_order_ref)
  WHERE psp_order_ref IS NOT NULL AND deleted_at IS NULL;

COMMENT ON INDEX idx_store_orders_psp_ref IS
  'Lookup orders by PSP-assigned reference number (for status callbacks)';

