-- Recent receive verifications
CREATE INDEX IF NOT EXISTS idx_receive_verifications_recent
  ON receive_verifications(verified_at DESC)
  WHERE deleted_at IS NULL;

COMMENT ON INDEX idx_receive_verifications_recent IS
  'Find most recent receive verifications across all assignments';

-- Receive verifications with damages (for quality tracking)
CREATE INDEX IF NOT EXISTS idx_receive_verifications_damages
  ON receive_verifications(verified_at DESC)
  WHERE received_damaged_qty > 0 AND deleted_at IS NULL;

COMMENT ON INDEX idx_receive_verifications_damages IS
  'Find receive verifications that reported damaged items';

-- Receive verifications with shortages (for quality tracking)
CREATE INDEX IF NOT EXISTS idx_receive_verifications_shortages
  ON receive_verifications(verified_at DESC)
  WHERE received_good_qty + received_damaged_qty < delivered_qty AND deleted_at IS NULL;

COMMENT ON INDEX idx_receive_verifications_shortages IS
  'Find receive verifications with quantity shortages';

-- Receive verifications metadata GIN index
CREATE INDEX IF NOT EXISTS idx_receive_verifications_metadata_gin
  ON receive_verifications USING GIN (verification_metadata)
  WHERE verification_metadata IS NOT NULL AND deleted_at IS NULL;

COMMENT ON INDEX idx_receive_verifications_metadata_gin IS
  'Query receive verifications by metadata (e.g., specific damage types or notes)';

