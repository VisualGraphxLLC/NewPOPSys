  ON order_lines(store_order_id, kit_item_id)
  WHERE deleted_at IS NULL;

COMMENT ON INDEX idx_order_lines_order_kit_item IS
  'Find specific line items in an order by kit item';

-- Order lines with quantity discrepancies (for quality monitoring)
CREATE INDEX IF NOT EXISTS idx_order_lines_qty_variance
  ON order_lines(store_order_id)
  WHERE (shipped_qty != ordered_qty OR delivered_qty != shipped_qty) AND deleted_at IS NULL;

COMMENT ON INDEX idx_order_lines_qty_variance IS
  'Find order lines with quantity discrepancies (shipped vs ordered, delivered vs shipped)';

