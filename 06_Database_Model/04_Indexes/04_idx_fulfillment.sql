-- =============================================================================
-- CROSS-TABLE FULFILLMENT QUERY PATTERNS
-- =============================================================================

-- Pattern 1: Campaign Order Status Dashboard
-- Query: Find all orders for a campaign with their shipment status
-- Uses: idx_store_orders_campaign_status + idx_shipments_order

-- Pattern 2: Store Fulfillment Timeline
-- Query: Track all orders, shipments, and receipts for a store assignment
-- Uses: idx_store_orders_assignment + idx_shipments_order + idx_receive_verifications_assignment

-- Pattern 3: PSP Callback Processing
-- Query: Lookup order and update shipment status based on PSP reference
-- Uses: idx_store_orders_psp_ref + idx_shipments_order

-- Pattern 4: Shipment Exception Management
-- Query: Find all shipments with exceptions and their associated orders
-- Uses: idx_shipments_exceptions + idx_shipments_order (reverse join)

-- Pattern 5: Quality Monitoring
-- Query: Find orders with damages, shortages, or quantity variances
-- Uses: idx_receive_verifications_damages + idx_receive_verifications_shortages + idx_order_lines_qty_variance

-- =============================================================================
-- PERFORMANCE NOTES
-- =============================================================================

-- 1. PSP integration indexes prioritize nullable columns (psp_order_ref can be NULL initially)
-- 2. Status-based partial indexes reduce index size for transient statuses
-- 3. Timestamp indexes use DESC for recent-first queries (most common pattern)
-- 4. GIN indexes on JSONB arrays (tracking_numbers) enable element-level searches
-- 5. Quality monitoring indexes use computed conditions in WHERE clause
-- 6. Composite indexes ordered by: foreign key → status → timestamp
